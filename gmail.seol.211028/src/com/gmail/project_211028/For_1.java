package com.gmail.project_211028;

public class For_1 {

	public static void main(String[] args) {
		int i = 0;
		while(i<3) {
			System.out.println();
			i= i+1;
		}
		
		for(int j = 0; j < 3; j=j+1) {
			System.out.println("image" + (j+1) + ".png");
		}
		
		int k = 0;
		for(k = 0; k < 3; k=k+1) {
			System.out.println("image" + (k+1) + ".png");
		}
		
	}

}
